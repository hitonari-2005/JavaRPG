package javaRPG.Character;

/**
 * RPGゲームにおけるすべてのキャラクターの基底クラス
 *
 * このクラスはゲーム内のあらゆるキャラクター（味方、敵、NPCなど）の
 * 共通部分を定義する抽象クラスです。
 *
 * 主な役割：
 * - キャラクターの基本情報（名前、ID、タイプ）を管理
 * - すべてのキャラクターに共通する行動（話す、移動する）を定義
 * - 継承によるオブジェクト指向設計の基盤を提供
 *
 * 継承関係：
 * Character → BattleUnit → AllyUnit/EnemyUnit → 具体的なキャラクタークラス
 *
 * @author RPG開発チーム
 * @version 1.0
 */
public abstract class Character {

    // ========================================
    // フィールド（属性）
    // ========================================

    /**
     * キャラクターの名前
     * 例：「勇者」「スライム」「村人A」など
     * 画面表示やメッセージで使用される
     */
    private String name;

    /**
     * キャラクターの識別ID
     * 各キャラクターを一意に識別するための番号
     * データベースやセーブデータでの管理に使用
     */
    private int id;

    /**
     * キャラクターのタイプ（種別）
     * 例：「Hero」「Mage」「Slime」など
     * キャラクターの分類や能力の判定に使用
     */
    private String type;

    // ========================================
    // コンストラクタ
    // ========================================

    /**
     * すべてのパラメータを指定してキャラクターを生成するコンストラクタ
     *
     * このコンストラクタは子クラスから呼び出され、
     * キャラクターの基本情報を初期化します。
     *
     * @param name キャラクターの名前
     * @param id キャラクターの識別ID
     * @param type キャラクターのタイプ
     */
    public Character(String name, int id, String type) {
        this.name = name;
        this.id = id;
        this.type = type;
    }

    /**
     * デフォルトコンストラクタ
     *
     * パラメータなしでキャラクターを生成する場合に使用されます。
     * デフォルト値で初期化されます：
     * - 名前：「名無し」
     * - ID：0
     * - タイプ：「一般人」
     */
    public Character() {
        this.name = "名無し";
        this.id = 0;
        this.type = "一般人";
    }

    // ========================================
    // メソッド（行動）
    // ========================================

    /**
     * キャラクターが話す行動
     *
     * キャラクターが自己紹介をするメッセージを表示します。
     * NPCとの会話イベントなどで使用されることを想定しています。
     *
     * 表示される内容：
     * - 「[名前]が話しかけてきた！」
     * - 「こんにちは！私は[タイプ]の[名前]です。」
     */
    public void talk() {
        System.out.println(name + "が話しかけてきた！");
        System.out.println("「こんにちは！私は" + type + "の" + name + "です。」");
    }

    /**
     * キャラクターが移動する行動
     *
     * キャラクターが移動したことを示すメッセージを表示します。
     * マップ移動システムやフィールド探索で使用されることを想定しています。
     *
     * 表示される内容：
     * - 「[名前]が移動した！」
     */
    public void move() {
        System.out.println(name + "が移動した！");
    }

    // ========================================
    // getter・setterメソッド
    // ========================================
    // キャラクターの各属性にアクセスするためのメソッド群
    // カプセル化の原則に従い、privateフィールドへのアクセスを制御

    /**
     * キャラクターの名前を取得
     *
     * @return キャラクターの名前
     */
    public String getName() {
        return name;
    }

    /**
     * キャラクターの名前を設定
     *
     * @param name 設定する名前
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * キャラクターのIDを取得
     *
     * @return キャラクターの識別ID
     */
    public int getId() {
        return id;
    }

    /**
     * キャラクターのIDを設定
     *
     * @param id 設定する識別ID
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * キャラクターのタイプを取得
     *
     * @return キャラクターのタイプ
     */
    public String getType() {
        return type;
    }

    /**
     * キャラクターのタイプを設定
     *
     * @param type 設定するタイプ
     */
    public void setType(String type) {
        this.type = type;
    }

}