package javaRPG.Interface;

/**
 * Sellable（売買可能）インターフェース
 *
 * このインターフェースは、商人の店で売買できるアイテムが
 * 実装すべき機能を定義します。
 *
 * 実装クラス：
 * - Sword（剣）: 攻撃力を上昇させる武器
 *   - ブロンズソード: 100G
 *   - アイアンソード: 300G
 *   - ミスリルソード: 800G
 *
 * - Staff（杖）: 魔法力を上昇させる武器
 *   - 木の杖: 150G
 *   - 魔導杖: 400G
 *   - 賢者の杖: 1000G
 *
 * 定義する機能：
 * 1. getPrice - アイテムの価格を取得
 * 2. getName - アイテムの名前を取得
 *
 * 使用場面：
 * - CommonMerchant（商人）クラスでアイテムを販売する際
 * - プレイヤーがショップでアイテムを購入する際
 * - 所持金と価格を比較して購入可否を判定する際
 *
 * @author RPG開発チーム
 * @version 1.0
 */
public interface Sellable {

    /**
     * アイテムの価格を取得するメソッド
     *
     * このアイテムの販売価格（ゴールド）を返します。
     * 商人の店でアイテムを購入する際、この価格分のお金が必要です。
     *
     * 使用例：
     * - ショップでアイテムリストを表示する際
     * - プレイヤーの所持金と比較して購入可否を判定する際
     * - 購入時に所持金から価格を減算する際
     *
     * @return アイテムの価格（ゴールド）
     */
    int getPrice();

    /**
     * アイテムの名前を取得するメソッド
     *
     * このアイテムの名前を返します。
     * ショップでのアイテム表示や、装備時のメッセージ表示に使用されます。
     *
     * 使用例：
     * - ショップでアイテムリストを表示する際
     * - アイテムを装備した際のメッセージ表示
     * - ステータス画面での装備品表示
     *
     * @return アイテムの名前
     */
    String getName();

} // Sellable インターフェース終了
