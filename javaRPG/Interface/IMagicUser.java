package javaRPG.Interface;

// 必要なクラスをインポート
import javaRPG.BattleUnit.BattleUnit;  // 戦闘ユニットの基底クラス
import java.util.List;                 // リストコレクション

/**
 * IMagicUser（魔法使用者）インターフェース
 *
 * このインターフェースは、魔法を使用できるキャラクターが
 * 実装すべき機能を定義します。
 *
 * 実装クラス：
 * - Mage（魔法使い）: 多彩な魔法を扱う魔法特化型
 *   - ファイア、ブリザド、サンダー、ケアル、サンダガ、メテオ
 *
 * - MagicSwordsman（魔法剣士）: 剣と魔法を扱える万能型
 *   - ファイア、サンダー、ケアル
 *
 * 定義する機能：
 * 1. useMagic - 指定された魔法を使用する
 *
 * 魔法の種類：
 * - 攻撃魔法: ファイア、ブリザド、サンダー、サンダガ、メテオ
 * - 回復魔法: ケアル
 * - 単体魔法: ファイア、ブリザド、サンダー、ケアル
 * - 全体魔法: サンダガ、メテオ
 *
 * 魔法力の計算：
 * 魔法の威力 = 基本攻撃力 + 杖の魔法力ボーナス
 *
 * @author RPG開発チーム
 * @version 1.0
 */
public interface IMagicUser {

    /**
     * 魔法を使用するメソッド
     *
     * 指定された名前の魔法を、指定された対象に対して使用します。
     * 対象は単体の場合もあれば、全体の場合もあります。
     *
     * 魔法の種類と効果：
     *
     * 【攻撃魔法】
     * - ファイア: 単体に炎属性の魔法ダメージ
     * - ブリザド: 単体に氷属性の魔法ダメージ
     * - サンダー: 単体に雷属性の魔法ダメージ（威力1.3倍）
     * - サンダガ: 全体に雷属性の魔法ダメージ
     * - メテオ: 全体に隕石を落とす強力な魔法（威力2倍）※Mageのみ
     *
     * 【回復魔法】
     * - ケアル: 単体のHPを回復
     *
     * ダメージ/回復量の計算：
     * - 基本値 = 基本攻撃力 + 杖の魔法力ボーナス
     * - 各魔法に応じて倍率が適用される
     *
     * @param magicName 使用する魔法の名前（日本語または英語）
     * @param targets 対象のリスト（単体の場合は1つ、全体の場合は複数）
     */
    void useMagic(String magicName, List<BattleUnit> targets);

} // IMagicUser インターフェース終了
